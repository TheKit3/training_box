import math

x = int(input("Введите число: "))


print(math.floor(x))  # округляет вниз
print(math.ceil(x))  # округлет вверх
print(abs(x))  # модуль (работает без math)
print(math.sqrt(x))  # квадратный корень
print(math.exp(x))  # вводит эксоненту в степень, равную числу
print(math.log(x))  # натуральный логорифм числа
print(math.log2(x))  # логафимп по основанию 2
print(math.log10(x))  # логафимп по основанию 10
print(math.sin(x))
print(math.cos(x))
